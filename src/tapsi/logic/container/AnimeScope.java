package tapsi.logic.container;

/**
 * <strong>IGNORE:</strong> If the user doesn't need any updates on this anime.<br />
 * <strong>MUSTHAVE:</strong> If the user would like to have updates on this anime.<br />
 * <strong>NOTDEFINED:</strong> If the user hasn't defined the anime yet.<br />
 */
public enum AnimeScope {
    IGNORE, MUSTHAVE, NOTDEFINED
}
